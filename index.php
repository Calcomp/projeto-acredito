			<div class="col-md-12 col-md-offset-0 post-body">
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;O que é o Projeto Acredito?</h4>
				<p>&Eacute; um programa de capta&ccedil;&atilde;o de recursos financeiros em conjunto dos estudantes do curso de bacharelado em Ci&ecirc;ncia da Computa&ccedil;&atilde;o da Funda&ccedil;&atilde;o Universidade Regional de Blumenau (FURB). Este visa a atualiza&ccedil;&atilde;o tecnol&oacute;gica da infraestrutura do curso (equipamentos e softwares), bem como a fomenta&ccedil;&atilde;o de eventos que tamb&eacute;m proporcionem a atualiza&ccedil;&atilde;o cient&iacute;fica para os alunos.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Como Funciona?</h4>
				<p>Todos os alunos matriculados no curso de Ci&ecirc;ncia da Computa&ccedil;&atilde;o pagam mensalmente um cr&eacute;dito financeiro. Esse dinheiro &eacute; arrecadado e mantido pela FURB, por&eacute;m, uma parte fica disposta &agrave; aplica&ccedil;&atilde;o de projetos desenvolvidos pelos alunos do curso; isto &eacute;, descentralizando sua aplica&ccedil;&atilde;o, que ser&aacute; feita de acordo com a demanda apresentada.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;...E se o Projeto Acredito não existisse?</h4>
				<p>Al&eacute;m de pagar pela quantia de cr&eacute;ditos financeiros vinculados ao curso, os alunos teriam ainda de pagar para poder utilizar os laborat&oacute;rios da Computa&ccedil;&atilde;o, realizar atividades ou cursos extracurriculares e at&eacute; mesmo para participar da Semana Acad&ecirc;mica. </p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Qual a vantagem do Projeto Acredito em relação ao Crédito Financeiro?</h4>
				<p>O dinheiro arrecadado pelo Cr&eacute;dito Financeiro &eacute; gerenciado exclusivamente pela FURB e est&aacute; fortemente vinculado com a manuten&ccedil;&atilde;o da institui&ccedil;&atilde;o, ou seja, &eacute; aplicado de forma geral e abrangente. J&aacute; o dinheiro arrecadado pelo Projeto Acredito &eacute; gerenciado pelo Conselho Deliberativo do Projeto Acredito juntamente com a Pr&oacute;-Reitoria de Administra&ccedil;&atilde;o - PROAD, direcionando, desta forma, os recursos diretamente para o curso de Computa&ccedil;&atilde;o. O fato de o dinheiro ser administrado por pessoas mais familiarizadas ao curso acaba beneficiando a todos os alunos. Estes poder&atilde;o ao longo de sua trajet&oacute;ria acad&ecirc;mica contar com uma infraestrutura sempre atualizada no que se refere ao parque tecnol&oacute;gico; al&eacute;m de ter a liberdade de apresentar eventuais demandas que acreditem se encaixarem na proposta do Projeto. </p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;O que já foi feito com o dinheiro arrecadado?</h4>
				<p>Entre 2010 e 2011, o Projeto Acredito junto com os recursos do Laborat&oacute;rio de Qualidade de Software (LQS), custeou tr&ecirc;s laborat&oacute;rios inteiros, dois com 15 m&aacute;quinas (campus 3) e um com 30 m&aacute;quinas (campus 1 - S-212). Destes, todos os monitores foram comprados com o dinheiro do Projeto Acredito, j&aacute; os gabinetes, teclados e mouses foram custeados pelo Laborat&oacute;rio de Qualidade de Software. Entre outras, o laborat&oacute;rio de rob&oacute;tica foi contemplado com novos Kits de rob&oacute;tica, os laborat&oacute;rios dos professores Miguel e Dalton receberam um novo projetor cada e o LARC (Laborat&oacute;rio de Redes de Computadores) recebeu novos equipamentos preparados para IP vers&atilde;o 6. O projeto tamb&eacute;m custeou eventos como: a Semana Acad&ecirc;mica e o Semin&aacute;rio de Inform&aacute;tica e Computa&ccedil;&atilde;o; incluindo-se aqui despesas com coffee breaks e as estadias dos palestrantes. Al&eacute;m de ter possibilitado a viagem dos alunos da Computa&ccedil;&atilde;o para o FLISOL (Festival Latino Americano de Instala&ccedil;&atilde;o de Software Livre) em Joinville.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Quem pode utilizar o dinheiro arrecadado?</h4>
				<p>O dinheiro pode ser utilizado nos projetos encaminhados pelos alunos matriculados no curso de bacharelado de Ci&ecirc;ncias da Computa&ccedil;&atilde;o da FURB e que, consequentemente, tenham sido aprovados pelo conselho Deliberativo do Projeto Acredito e autorizados pela Pr&oacute;-Reitoria de Administra&ccedil;&atilde;o (PROAD). </p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Por que o Acredito não financia eventos como Stamm Universitário?</h4>
				<p>Em primeira inst&acirc;ncia utilizar recursos para tal fim infringe o estatuto do Projeto Acredito, em que o dinheiro arrecadado deve ser destinado a projetos que agreguem algum conhecimento ou tragam algum benef&iacute;cio a um maior n&uacute;mero de estudantes do curso. Embora entendamos a import&acirc;ncia de eventos que promovem uma maior integra&ccedil;&atilde;o entre a comunidade universit&aacute;ria, esta tem&aacute;tica foge da proposta do Projeto. Deste modo, a destina&ccedil;&atilde;o de recursos para eventos de cunho unicamente festivo, estaria indo contra a proposta inicialmente apresentada e aprovada, e consequentemente estar&iacute;amos incorrendo em uma ilegalidade. Al&eacute;m disso, por ser p&uacute;blica, a FURB deve prestar contas de toda e qualquer despesa, inclusive dos recursos do Projeto Acredito.</p>
