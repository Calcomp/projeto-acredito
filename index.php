			<div class="col-md-12 col-md-offset-0 post-body">
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;O que é o Projeto Acredito?</h4>
				<p>É um programa de captação de recursos financeiros junto aos alunos do curso Bacharel em Ciência da Computação da Fundação Universidade
					Regional de Blumenau (FURB), visando a atualização tecnológica da infraestrutura do curso (equipamentos e softwares),
					além de viabilizar o auxílio a eventos que permitam também a atualização científica dos nossos alunos.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Como Funciona?</h4>
				<p>Cada aluno matriculado no curso de Ciência da Computação paga mensalmente 1 (um) crédito financeiro. Esse dinheiro é
					mantido pela FURB, porém, sua aplicação fica a disposição de projetos encaminhados pelos alunos, descentralizando desta
					forma sua aplicação, que será feita de acordo com a demanda apresentada.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;...E se o Projeto Acredito não existisse?</h4>
				<p>Os alunos teriam que pagar a quantia de créditos financeiros vinculados ao curso para a utilização dos laboratórios,
					além de eventos como Semanas Acadêmica e de Extensão.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Qual a vantagem do Projeto Acredito em relação ao Crédito Financeiro?</h4>
				<p>O dinheiro arrecadado pelo Crédito Financeiro é gerenciado exclusivamente pela FURB e está fortemente vinculado com a
					manutenção da instituição, ou seja, é aplicado de forma geral e abrangente. Já o dinheiro arrecadado com o Projeto Acredito,
					é gerenciado pelo Conselho Deliberativo do Projeto Acredito juntamente com a Pró-Reitoria de Administração - PROAD,
					direcionando desta forma estes recursos diretamente para o curso de Computação. Tal fato acaba por beneficiar todos
					os alunos, que podem contar ao longo do curso com uma infraestrutura sempre atualizada no que se refere ao parque tecnológico
					como também eventuais outras demandas que venham a ser apresentadas pelos alunos no decorrer da graduação.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;O que já foi feito com o dinheiro arrecadado?</h4>
				<p>Entre 2010 e 2011, o Projeto Acredito, junto com os recursos do Laboratório de Qualidade de Software (LQS), custeou três
					laboratórios inteiros, dois de 15 máquinas (campus 3) e um de 30 máquinas (campus 1 - S-212). Destes, todos os monitores
					foram comprados com o dinheiro do Projeto Acredito, já os gabinetes, teclados e mouses foram custeados pelo Laboratório
					de Qualidade de Software. Entre outras, o laboratório de robótica também foi contemplado com novos Kits de robótica,
					os laboratórios dos professores Miguel e Dalton receberam um novo projetor cada e o LARC (Laboratório de redes de computadores)
					também recebeu novos equipamentos preparados para IP versão 6. O projeto também custeou eventos como: Semana Acadêmica
					e SEMINCO (coffe break estadias dos palestrantes). Ainda foi utilizado para levar os alunos da Computação ao FLISOL
					em Joinville.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Quem pode utilizar o dinheiro arrecadado?</h4>
				<p>O dinheiro pode ser utilizado por projetos que tenham sido aprovados pelo conselho Deliberativo do Projeto Acredito e
					autorizados pela PROAD.</p>
				<hr>
				<h4><i class="fa fa-chevron-circle-right"></i>&nbsp;Por que o Acredito não financia eventos como Stamm Universitário?</h4>
				<p>Além de infringir o estatuto do Projeto Acredito, o dinheiro arrecadado é destinado a <strong>projetos que agreguem</strong> algum conhecimento
					ou <strong>tragam algum benefício a um maior número de estudantes</strong> do curso. Embora entendamos a importância de eventos desta
					natureza, que promovem uma maior integração entre a comunidade universitária, a temática foge da proposta de criação
					do Acredito, apresentada e aprovada pela Universidade. De modo que a destinação de recursos para eventos de cunho unicamente
					festivo, por mais que gostemos de festejar, estaria indo contra a proposta inicialmente apresentada e aprovada e consequentemente
					estaríamos incorrendo em uma ilegalidade. Soma-se, ainda, o fato de a Universidade ser pública e como tal deve prestar
					contas de toda e qualquer despesa, inclusive dos recursos do Acredito.</p>
